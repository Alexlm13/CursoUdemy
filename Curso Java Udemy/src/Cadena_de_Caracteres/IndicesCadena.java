package Cadena_de_Caracteres;

public class IndicesCadena {
    public static void main(String[] args) {
        String cadena1="Hola Mundo";

        char primerCaracter=cadena1.charAt(0);
        System.out.println(primerCaracter);

        char ultimoCaracter=cadena1.charAt(9);
        System.out.println(ultimoCaracter);

        char letraM=cadena1.charAt(5);
        System.out.println(letraM);
    }
}
