package Bucles;

public class DOSNumerosPares {
    public static void main(String[] args) {
        System.out.println("*** Numeros pares de 1 al 20 ***");

        int cont=1;
        while(cont <= 20){
            if (cont%2==0) 
                System.out.print(cont+" ");
                cont++;
            
            
        }
    }
}
