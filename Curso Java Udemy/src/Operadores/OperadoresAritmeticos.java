package Operadores;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
        System.out.println("*** Operadores Aritméticos ***");
        int a= 5, b=3, resultado=0;

        //suma
        resultado=a+b;
        System.out.println(resultado);
        //resta
        resultado=a-b;
        System.out.println(resultado);
        //multiplicacion
        resultado=a*b;
        System.out.println(resultado);
        //division
        resultado=a/b;
        System.out.println(resultado);
        //resto de la division (módulo)
        resultado=a%b;
        System.out.println(resultado);
    }
}
